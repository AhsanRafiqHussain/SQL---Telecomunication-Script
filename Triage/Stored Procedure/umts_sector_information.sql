		,pm.begin_time
		,pm.end_time
		,pm.years
		,pm.months
		,pm.weeks
		,pm.days
		,pm.hours
		,pm.minutes
		,sd.sector_classification 
	FROM umts_pm_1 AS pm
	LEFT JOIN sd_umts AS sd ON pm.cell = sd.sd_cell_name
		--AND pm.nodeb = sd.sd_nodeb_name
		AND pm.rnc = sd.sd_rnc_name;
		
	drop table if exists sd_umts;
	drop table if exists umts_pm_1;
	drop table if exists umts_rnc;	

