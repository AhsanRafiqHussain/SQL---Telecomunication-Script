	--TABLE (region1 varchar, market1 varchar,zone1 varchar,rnc_id1 int,rnc1 varchar,nodeb_id1 int, nodeb1 varchar, cell_id1 int,cell1 text,begin_time1 timestamp,
	--end_time1 timestamp,years1 int,months1 int,weeks1 int,days1 int,hours1 int,minutes1 int)
