	ON COMMIT PRESERVE ROWS AS
SELECT DISTINCT a.cellid
	,a.cellname
	,ltrim(rtrim(a.nodebname)) nodebname
	,ltrim(rtrim(a.neid)) neid
	,cast(a.uarfcndownlink AS INT) AS uarfcndownlink
--	,cast(locell as int) locell
FROM huawei_cm_3g_ucell a
WHERE DATE (DATETIME) = DATE (_date);


drop table if exists df_rnc;
	CREATE TEMP TABLE df_rnc ON
	COMMIT preserve rows AS
	SELECT DISTINCT ltrim(rtrim(neid)) neid
	,rncid
FROM huawei_cm_3g_urncbasic;

drop table if exists df_nodeb;
	CREATE TEMP TABLE df_nodeb ON
COMMIT preserve rows AS
SELECT DISTINCT ltrim(rtrim(neid)) neid
	,cast(nodebid AS INT) nodeb_id
	,ltrim(rtrim(nodebname)) nodebname
FROM huawei_cm_3g_unodeb
WHERE DATE (DATETIME) = DATE (_date);



drop table if exists umts_pm_1;
	CREATE TEMP TABLE umts_pm_1 ON
	COMMIT preserve rows AS
	SELECT DISTINCT '3G' AS technology
	,cast(pm.cellid AS INT) AS cell_id
	,pm.label AS cell
	,umts_cm.nodebname AS nodeb
	--,umts_cm.locell 
	,ltrim(rtrim(pm.netype)) AS rnc
	,umts_cm.uarfcndownlink
	,pm.begintime AS begin_time
	,pm.endtime AS end_time
	,pm.begintime_in_years AS years
	,pm.begintime_in_months AS months
	,pm.begintime_in_weeks AS weeks
	,extract(EPOCH FROM DATE (pm.begintime) AT TIME ZONE 'UTC')::INT / 86400 AS days
	--,pm.begintime_in_days AS days
	--,pm.begintime_in_hours AS hours
	,extract(EPOCH FROM (pm.begintime) AT TIME ZONE 'UTC')::INT / 3600 AS hours
	--,pm.begintime_in_minutes AS minutes
	,extract(EPOCH FROM (pm.begintime) AT TIME ZONE 'UTC')::INT / 60 AS minutes
FROM huawei_pm_3g_algo_cell AS pm
LEFT JOIN umts_cm ON pm.cellid = umts_cm.cellid
	AND pm.label = umts_cm.cellname
	AND pm.netype = umts_cm.neid
WHERE 
--pm.label NOT ilike '%Toy%' AND
	 pm.begintime = _date;

drop table if exists umts_cm;

drop table if exists sd_umts;
CREATE TEMP TABLE sd_umts ON
COMMIT preserve rows AS
SELECT DISTINCT sd_cell_name
	,sd_nodeb_name
	,sd_rnc_name 
	,sd_cluster
	,sd_province 
	,sd_region 
	,classification as sector_classification
FROM site_data_umts a
left join sector_classification_core_border b 
on a.sd_cell_name = b.cell;



	drop table if exists umts_pm_sd;
	CREATE TEMP TABLE umts_pm_sd ON
	COMMIT preserve rows AS
