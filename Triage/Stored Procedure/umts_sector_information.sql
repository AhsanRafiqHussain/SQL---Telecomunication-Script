 --1- Sector 
	---------------------------------------------------------------------
	_begintime:= (SELECT distinct begintime::time
	FROM huawei_pm_3g_algo_cell
		where begintime = _date);
	_endtime:= (SELECT distinct endtime::time  -  interval '1 second'
	FROM huawei_pm_3g_algo_cell
		where begintime = _date);
		
drop table if exists umts_cm;
