	COMMIT preserve rows AS
	SELECT DISTINCT pm.technology
		,pm.cell_id
		,pm.cell
		,pm.nodeb
		,r.rncid AS rnc_id
		,pm.rnc
		,zone
		,market
		,region
		,uarfcn
		--,pm.locell
		,pm.begin_time
		,pm.end_time
		,pm.years
		,pm.months
		,pm.weeks
		,pm.days
		,pm.hours
		,pm.minutes
		,pm.sector_classification
	FROM umts_pm_sd AS pm
	LEFT JOIN df_rnc r ON pm.rnc = r.neid;

