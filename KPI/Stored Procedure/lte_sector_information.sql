	case when (a.region is null) 
		then (select region_db_id from lte_region where region is null) 
		else (select region_db_id from lte_region where region = a.region) end
	,a.region
	,case when (a.market is null) 
		then (select market_db_id from lte_market where market is null) 
		else (select market_db_id from lte_market where market = a.market) end
	,a.market
	,case when(a.zone is null) 
		then (select zone_db_id from lte_zone where zone is null) 
		else (select zone_db_id from lte_zone where zone = a.zone) end
	,a.zone
	,case when( a.tac_id is null) 
		then (select tac_db_id from lte_tac where tac_id is null) 
		else (select tac_db_id from lte_tac where tac_id = a.tac_id) end
	,a.tac_id
	,case when( a.enodeb is null) 
		then (select enodeb_db_id from lte_enodeb where enodeb is null) 
		else (select enodeb_db_id from lte_enodeb where enodeb_id = a.enodeb_id and enodeb = a.enodeb) end
	,a.enodeb_id
