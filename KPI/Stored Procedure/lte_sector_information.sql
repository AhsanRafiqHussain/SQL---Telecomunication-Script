		then (select sector_db_id from lte_sector where sector is null) 
		else (select sector_db_id from lte_sector where sector_id = a.local_cell_id and sector = a.cell_name) end --1
	,a.local_cell_id
	,a.cell_name
	FROM LTE_PM a 
	where not exists ( select id from lte_sector_information b 
		WHERE b.region_db_id  = (case when (a.region is null) 
									then (select region_db_id from lte_region where region is null) 
									else (select region_db_id from lte_region where region = a.region) end)
		and b.market_db_id =  	(case when (a.market is null) 
									then (select market_db_id from lte_market where market is null) 
									else (select market_db_id from lte_market where market = a.market)end )
		and b.zone_db_id = 		(case when(a.zone is null) 
									then (select zone_db_id from lte_zone where zone is null) 
									else (select zone_db_id from lte_zone where zone = a.zone) end)
		and b.tac_db_id = 		(case when( a.tac_id is null) 
									then (select tac_db_id from lte_tac where tac_id is null) 
									else (select tac_db_id from lte_tac where tac_id = a.tac_id) end)
		and b.enodeb_db_id = 	(case when( a.enodeb is null) 
									then (select enodeb_db_id from lte_enodeb where enodeb is null) 
									else (select enodeb_db_id from lte_enodeb where enodeb_id = a.enodeb_id and enodeb = a.enodeb) end)
		and b.sector_db_id = 	(case when( a.cell_name is null) 
									then (select sector_db_id from lte_sector where sector is null) 
									else (select sector_db_id from lte_sector where sector_id = a.local_cell_id and sector = a.cell_name) end)) --2
	Group by a.region
	,a.market
	,a.zone
	,a.tac_id
	,a.enodeb_id
	,a.enodeb
	,a.local_cell_id
	,a.cell_name;
