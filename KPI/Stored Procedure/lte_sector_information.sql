	,pm.local_cell_id
	,pm.cell_name
	,cast(pm.enodeb_id as varchar)
	,pm.enodeb_function_name AS enodeb
	--,coalesce(pm.enodeb_function_name,sd.sd_enodeb_name) as enodeb
	,sd.sd_tac AS tac_id
	,sd.sd_cluster AS zone
	,sd.sd_province AS market
	,sd.sd_region AS region
	,pm.dlearfcn as earfcn
	-- ,coalesce(pm.dlearfcn, cast(sd.sd_earfcn_dl AS INT)) AS earfcn
	,pm.begin_time
	,pm.end_time
	,pm.years
	,pm.months
	,pm.weeks
	,pm.days
	,pm.hours
	,pm.minutes
FROM lte_pm_2 AS pm
LEFT JOIN site_data_lte AS sd ON pm.cell_name = sd.sd_cell_name
	AND pm.enodeb_function_name = sd.sd_enodeb_name;


	
	insert into lte_region(region, is_active)
	select a.region , true from LTE_PM a 
	where not exists (select region_db_id from lte_region b where region_db_id = (case when (a.region is null) 
									then (select region_db_id from lte_region where region is null) 
									else (select region_db_id from lte_region where region = a.region) end))
	group by a.region;
	
	insert into lte_market(market, is_active)
